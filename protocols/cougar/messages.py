class CougarTCPModel:
    """Enhanced TCP transfer time calculation with real RTT data."""
    
    def __init__(self, geo_provider=None):
        self.initial_window_size = 536 * 4  # 4 MSS segments (2144 bytes)
        self.mss = 536  # Maximum Segment Size
        self.geo_provider = geo_provider  # ✅ GeoDataProvider reference
    
    def calculate_transfer_rtts(self, data_size_bytes: int) -> float:
        """
        Calculate number of RTTs needed for data transfer.
        
        Based on TCP congestion window growth:
        - If data fits in initial window: 0.5 RTT
        - Otherwise: 0.5 + additional RTTs for extra batches
        
        Args:
            data_size_bytes: Size of data to transfer
            
        Returns:
            Number of RTTs required
        """
        if data_size_bytes <= self.initial_window_size:
            print(f"DEBUG: Single batch, returning 0.5")
            return 0.5  # Single batch transfer
        
        # Calculate additional batches needed
        remaining_bytes = data_size_bytes - self.initial_window_size
        current_window = self.initial_window_size
        additional_rtts = 0
        
        while remaining_bytes > 0:
            additional_rtts += 1
            # TCP additive increase: window grows by MSS each RTT
            current_window += self.mss
            remaining_bytes -= current_window

            print(f"DEBUG: Final RTTs needed = {additional_rtts}")
        
        return 0.5 + additional_rtts
    
    def get_real_rtt(self, city1: str, city2: str) -> float:
        """Get real RTT between cities using GeoDataProvider."""
        if self.geo_provider:
            return self.geo_provider.get_rtt(city1, city2)  # ✅ Gerçek RTT verisi
        else:
            return 100.0  # Fallback
    
    def get_header_transfer_time(self, rtt_ms: float) -> float:
        """Calculate header transfer time (always fast)."""
        return 0.5 * rtt_ms
    
    def get_body_request_time(self, rtt_ms: float) -> float:
        """Calculate body request time."""
        return 0.5 * rtt_ms
    
    def get_body_transfer_time(self, body_size: int, rtt_ms: float) -> float:
        """Calculate body transfer time based on size."""
        rtts_needed = self.calculate_transfer_rtts(body_size)
        return rtts_needed * rtt_ms
    
    def get_timeout_duration(self, body_size: int, rtt_ms: float) -> float:
        """
        Calculate timeout for body requests.
        Paper specifies: 2 × RTTs required for transfer
        """
        body_transfer_time = self.get_body_transfer_time(body_size, rtt_ms)
        return 2.0 * body_transfer_time
    
    def get_timeout_duration_with_cities(self, body_size: int, sender_city: str, receiver_city: str) -> float:
        """Calculate timeout using real RTT data between cities."""
        real_rtt_ms = self.get_real_rtt(sender_city, receiver_city)  # ✅ Gerçek RTT
        body_transfer_time = self.get_body_transfer_time(body_size, real_rtt_ms)
