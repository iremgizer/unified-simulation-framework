"""
Cougar Protocol Message Types

Implements message classes for the Cougar protocol, following the same pattern
as Kadcast messages but adapted for Cougar's header-first dissemination model.

Message Flow:
1. CougarHeaderMessage: Block header advertisement (push)
2. CougarBodyRequestMessage: Body pull request  
3. CougarBodyResponseMessage: Block body transfer
4. CougarTimeoutEvent: Handled in simulator (not network message)
"""

from typing import Dict,List, Any, Optional
import struct
import json


class CougarMessage:
    """
    Base class for all Cougar protocol messages.
    Following Kadcast pattern for consistency.
    """
    
    def __init__(self, message_type: str, sender_id: str):
        self.message_type = message_type
        self.sender_id = sender_id
        self.timestamp = 0
        self.size = 0  # Will be calculated in subclasses
    
    def to_dict(self) -> Dict[str, Any]:
        """Convert message to dictionary for logging/debugging."""
        return {
            "message_type": self.message_type,
            "sender_id": self.sender_id,
            "timestamp": self.timestamp,
            "size": self.size
        }
    
    def _calculate_size(self) -> int:
        """Calculate message size in bytes. Override in subclasses."""
        raise NotImplementedError


class CougarHeaderMessage(CougarMessage):
    """
    Header advertisement message (push to all neighbors).
    
    Contains block header data generated by RealisticBlockGenerator.
    Size: Actual serialized header size (not fixed!)
    """
    
    def __init__(self, sender_id: str, block_hash: str, header_data: Dict[str, Any]):
        super().__init__("HEADER_ADVERTISEMENT", sender_id)
        self.block_hash = block_hash
        self.header_data = header_data
        
        # Use actual RealisticBlockGenerator serialization
        self.header_bytes = json.dumps(header_data).encode('utf-8')
        self.size = self._calculate_size()
    
    def _calculate_size(self) -> int:
        """Use actual serialized header size from RealisticBlockGenerator."""
        return len(self.header_bytes)
    
    def to_dict(self) -> Dict[str, Any]:
        base = super().to_dict()
        base.update({
            "block_hash": self.block_hash,
            "header_size": len(self.header_bytes)
        })
        return base


class CougarBodyRequestMessage(CougarMessage):
    """
    Body pull request message.
    
    Sent from downstream peer to upstream peer to request block body.
    Size: 32 bytes (block hash)
    """
    
    def __init__(self, sender_id: str, target_peer_id: str, block_hash: str):
        super().__init__("BODY_REQUEST", sender_id)
        self.target_peer_id = target_peer_id
        self.block_hash = block_hash
        self.size = self._calculate_size()
    
    def _calculate_size(self) -> int:
        """Request message contains only block hash."""
        return 32  # SHA-256 hash
    
    def to_dict(self) -> Dict[str, Any]:
        base = super().to_dict()
        base.update({
            "target_peer_id": self.target_peer_id,
            "block_hash": self.block_hash
        })
        return base


class CougarBodyResponseMessage(CougarMessage):
    """
    Body response message containing full block body.
    
    Contains transaction data generated by RealisticBlockGenerator.
    Size: Variable (depends on transaction count and sizes from realistic_payload)
    """
    
    def __init__(self, sender_id: str, target_peer_id: str, block_hash: str, 
                 transactions: List[Dict[str, Any]]):
        super().__init__("BODY_RESPONSE", sender_id)
        self.target_peer_id = target_peer_id
        self.block_hash = block_hash
        self.transactions = transactions
        
        # Use actual RealisticBlockGenerator transaction serialization
        self.body_data = json.dumps(transactions).encode('utf-8')
        self.size = self._calculate_size()
    
    def _calculate_size(self) -> int:
        """Body size is the actual serialized transaction data from realistic_payload."""
        return len(self.body_data)
    
    def to_dict(self) -> Dict[str, Any]:
        base = super().to_dict()
        base.update({
            "target_peer_id": self.target_peer_id,
            "block_hash": self.block_hash,
            "body_size": len(self.body_data),
            "transaction_count": len(self.transactions)
        })
        return base



